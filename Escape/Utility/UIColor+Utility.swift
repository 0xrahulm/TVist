import UIKit
extension UIColor{
    
    class func themeColorBlue() -> UIColor{
        return UIColor(red:0.26, green:0.77, blue:0.72, alpha:1.0)//#43C5B8
        
    }
    class func themeColorRed() -> UIColor{
        return UIColor(red:0.96, green:0.39, blue:0.39, alpha:1.0) //#F56363
    }
    
}

